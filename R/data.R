#' Monthly Visitor Arrivals from 1998
#'
#' Visitor arrivals by country of residence all travel mode (Monthly)
#'
#' Visitor arrivals are overseas residents arriving in New Zealand
#' for a stay of less than 12 months.
#' For arrival series, the country of residence is the country where
#' a person arriving in New Zealand last lived for 12 months or
#' more (country of last permanent residence).
#'
#' The United Kingdom also includes the Channel Islands and the
#' Isle of Man.
#'
#' Cruise passengers have only been collected separately since
#' September 1997.
#' Cruise passengers are only recorded as arrivals or departures
#' if they begin or end their cruise in New Zealand.
#' Otherwise, they are regarded as being in transit,
#' even if they leave the ship for periods while it is in New Zealand.
#'
#' @source Statistics New Zealand
"week8_Actual_VisitorArrivalsMonthlyFrom1998"

